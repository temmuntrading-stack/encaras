export function translate(text: string | undefined | null): string {
  if (!text) return "";
  const trimmed = text.trim();
  return KOR_TO_MGL[trimmed] || trimmed;
}

export function translateTextContaining(text: string): string {
    if (!text) return "";
    let trText = text;
    for (const [kr, mn] of Object.entries(KOR_TO_MGL)) {
        trText = trText.replace(new RegExp(kr, 'g'), mn);
    }
    return trText;
}
